package javaPackage;

public class Var_18LoopFor1 {

	public static void main( String[] args) {
		
		//for loop int i=0; this initialization or VarName / VarValue, i>5 Condition to true, i++ increment then again condition till condition get false.
		for (int i = 0; i <= 5; i++) {
			System.out.println("value of i is : " + i);
		}
		System.out.println("control will be come out from LOOP");

	}

}
