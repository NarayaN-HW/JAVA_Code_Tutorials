package javaPackage;

public class Var_4CallingFun {
	public static void main(String[] args) {
		
		int a = 1000;
		
		//calling a variable  for printing values
		System.out.println(a);
		
		//calling a variable initialing with b
		int b = a;
		System.out.println(b);	
		
		int c; // declaring variable local variable 
		c = 101; // c assign value
		System.out.println(c); 
		
		int d = 1; // defining variable local variable
		System.out.println(d);
	}

}
