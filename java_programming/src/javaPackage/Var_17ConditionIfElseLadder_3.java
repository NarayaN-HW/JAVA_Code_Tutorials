public class Var_17ConditionIfElseLadder_3 {

	public static void main(String[] args) {
		int x = 200;
		int y = 400;
		
		if (x > y) {
			System.out.println("x is greater");
		} else if (x == y) {
			System.out.println(" x is equal to y");
		} else {
			System.out.println("x is smaller than y");
		}

	}

}
