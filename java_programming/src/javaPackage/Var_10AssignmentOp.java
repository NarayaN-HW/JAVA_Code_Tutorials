package javaPackage;

public class Var_10AssignmentOp {

	public static void main(String[] args) {
		
		int a = 10; 
		a += 3; // not pass space between + & =
		System.out.println(a); // 13
		
		a -= 3;
		System.out.println(a); // 10
		
		a *= 3;
		System.out.println(a); // 30
		
		a /= 3;
		System.out.println(a); // 10

	}

}
