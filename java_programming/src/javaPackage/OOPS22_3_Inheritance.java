package javaPackage;


// 2 Single Level Inheritance
public class OOPS22_3_Inheritance {
	
	int z;
	
	public void adding(int a, int b) {
		z = a + b;
		
		System.out.println("Addition is : " + z);
	}
	
	public void divide(int a, int b) {
		z = a / b;
		
		System.out.println("Division is : " + z);
	}
	
	public void modulas(int a, int b) {
		z = a % b;
		
		System.out.println("Modulas is : " + z);
	}
	

}
