package javaPackage;

public class Var_8ArithmeticOp {

	public static void main(String[] args) {
		
		int a = 10;   // def local var 
		int b = 5;    // defining local var
		
		System.out.println(a + b); // add 
		System.out.println(a - b); // sub
		System.out.println(a * b); // mul
		System.out.println(a / b); // div
		System.out.println(a % b); // modulas
		System.out.println(a ^ b); // dont no search bhai 
		System.out.println(a & b); // bitwise "always check all value/conditions" dont no in maths logic
		System.out.println(b = a); // if b=a :10, & i written a=b then :5 O/P
		
	}

}
