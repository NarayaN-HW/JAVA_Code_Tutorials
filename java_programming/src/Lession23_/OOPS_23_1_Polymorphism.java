//method OverLoading
public class OOPS_23_1_Polymorphism {
	
	public void sum(int a, int b) { // passing 2 parameter a,b
		System.out.println(a + b);
	}
	public void sum(int a, int b, int c) { // passing 3 parameter a,b,c
		System.out.println("total sum of : "+ a + b + c); // if you adding "string" o/p will be add number not sum
	}

}
